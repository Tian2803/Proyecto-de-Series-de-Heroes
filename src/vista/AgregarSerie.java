    private void recargarSeries(){
        this.txtNombreSerie.setText("");
        this.txtAnioCreacion.setText("");
        ArrayList <String> datosSeries = new ArrayList<>();
        if(manipulador!=null){
            for(Serie serie: manipulador.getSeries()){
                datosSeries.add(serie.getNombreSerie()+"---"+serie.getYearCreacion()+"\n");
            }
            DefaultListModel listModel = new DefaultListModel();
            for (int i = 0; i < datosSeries.size(); i++)
            {
                listModel.addElement(datosSeries.get(i));
            }
            this.txtSeriesExistentes.setModel(listModel);            
        }                    
    }
