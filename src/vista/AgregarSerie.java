    private void btnRegistrarSerieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarSerieActionPerformed
        try{
            String nombreSerie = txtNombreSerie.getText();
            int anioCreacion = Integer.parseInt(txtAnioCreacion.getText());
            if(!nombreSerie.equals("") && anioCreacion>1700){
                if(manipulador.agregarSerie(new Serie(nombreSerie,anioCreacion))){
                    escribirArchivoSeries();
                }
                else{                    
                    JOptionPane.showMessageDialog(null, "Ya existe una serie con estos datos","",JOptionPane.WARNING_MESSAGE);
                }
                recargarSeries();
            }
            else{
                JOptionPane.showMessageDialog(null,"Todos los cambos deben ser llenados","Error datos",JOptionPane.WARNING_MESSAGE);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Hubo un problema: " + e,"Error datos",JOptionPane.WARNING_MESSAGE);
        }
            
    }//GEN-LAST:event_btnRegistrarSerieActionPerformed

    private void escribirArchivoSeries(){
        EscribirManipulaPro escribir = new EscribirManipulaPro();
        escribir.abrirArchivo();
        escribir.escribirObjeto(manipulador);
        escribir.cerrarArchivo();
    }
